"""End-to-end matrix coverage: every row of the #75 status-code
matrix exercised through every fallback operation via the *real*
``huggingface_hub`` API (``hf_hub_download``, ``HfApi.model_info``,
``HfApi.list_repo_files``, ``HfApi.get_paths_info``,
``HfApi.file_exists``).

Why this is exhaustive rather than a unit-test layer:

- The classifier itself is unit-tested in ``test_utils.py``.
- The per-op binding logic is unit-tested in ``test_operations.py``.
- The interop layer (driving ``hf_raise_for_status`` directly with
  rehydrated FastAPI responses) is in ``test_hf_hub_interop.py``.

What's left, and what *this* file proves: that a real
``huggingface_hub`` client talking to a real KohakuHub uvicorn
process — which in turn talks to a real upstream uvicorn (the
parametric scenario mock) — observes the right exception class /
download success for every cell of the matrix.

The test set is generated by parametrizing one test per
(operation × matrix-row); each row's expected outcome is encoded
once and shared across operations. ``ALL_SCENARIOS`` is the master
list — adding a row here means every operation gets covered for it.
"""

