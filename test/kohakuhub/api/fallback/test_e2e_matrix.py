# --- Resolve flow (hf_hub_download) ---
#
# ``hf_hub_download`` has a head-call-error wrapper at
# ``huggingface_hub/file_download.py:_raise_on_head_call_error`` that
# *only* propagates these specific exception classes (plus 401-status
# generic errors):
#
#   RevisionNotFoundError, RemoteEntryNotFoundError → propagate
#   GatedRepoError, RepositoryNotFoundError         → propagate
#   401-status HfHubHTTPError                       → propagate
#   anything else (DisabledRepoError, 403, 5xx)     → wrap as
#                                                     LocalEntryNotFoundError
#
# So the matrix below differs from the raw classifier matrix in three
# rows: ``disabled``, ``403_bare``, ``503`` end up as
# ``LocalEntryNotFoundError`` even though the upstream/aggregate
# response is a DisabledRepo / 403 / 502 respectively. Other
# operations (model_info, list_repo_files, get_paths_info) call
# ``hf_raise_for_status`` directly without the wrapper and see the
# classifier-natural exception classes — see INFO_MATRIX etc. below.
#
# 5xx is omitted from this matrix because hf_hub_download retries
# 5xx 5 times with exponential backoff (~30s); the unit-layer tests
# in test_operations.py already cover that the aggregate is 502 and
# test_hf_hub_interop.py proves the classifier path is correct.
RESOLVE_MATRIX = [
    pytest.param("200_ok",                "success",                  id="200-ok"),
    # ``307_canonical_redirect`` intentionally absent: HF routes
    # ``/{name}/resolve/...`` directly to the canonical-named
    # storage rather than emitting a 307. Verified 2026-04-30 against
    # ``HEAD /bert-base-uncased/resolve/main/config.json`` — the
    # response is 307 to the resolve-cache CDN (a different shape,
    # covered by ``307_resolve_cache``), not to a canonical-name
    # path. Canonical redirect lives only on the API surface
    # (info / tree / paths-info).
    pytest.param("307_resolve_cache",     "success",                  id="307-resolve-cache"),
    pytest.param("404_entry_not_found",   "EntryNotFoundError",       id="404-entry-not-found-PROPAGATE"),
    pytest.param("404_revision_not_found","RevisionNotFoundError",    id="404-revision-not-found-PROPAGATE"),
    # ``disabled`` now routes to TRY_NEXT_SOURCE per the updated
    # policy (a moderation takedown on one source doesn't bind the
    # chain — sibling sources may still serve). With a single source
    # the aggregate preserves the disabled marker; hf_hub raises
    # DisabledRepoError, which hf_hub_download's wrapper does NOT
    # propagate (it's not in its allowlist), so it surfaces as
    # LocalEntryNotFoundError end-to-end.
    pytest.param("disabled",              "LocalEntryNotFoundError",  id="disabled-aggregate-wrapped"),
    pytest.param("404_repo_not_found",    "RepositoryNotFoundError",  id="404-repo-not-found-aggregate"),
    pytest.param("401_gated",             "GatedRepoError",           id="401-gated-aggregate"),
    pytest.param("403_gated",             "GatedRepoError",           id="403-gated-aggregate"),
    pytest.param("401_bare_anti_enum",    "RepositoryNotFoundError",  id="401-bare-anti-enum-aggregate"),
    pytest.param("401_bare_invalid_creds","RepositoryNotFoundError",  id="401-bare-invalid-creds-aggregate"),
    pytest.param("403_bare",              "LocalEntryNotFoundError",  id="403-bare-aggregate-wrapped"),
    pytest.param("404_bare",              "EntryNotFoundError",       id="404-bare-aggregate-scope-file"),
    # ``503`` omitted: hf_hub_download retries 5xx with backoff
    # (~30s/test). Aggregate-to-502 path is covered by
    # test_operations.py and test_hf_hub_interop.py instead.
]


def _maybe_skip_disabled(expected: str) -> None:
    if expected == "DisabledRepoError":
        try:
            _hf_error("DisabledRepoError")
        except (AttributeError, ImportError):
            pytest.skip("DisabledRepoError not exported by this hf_hub version")


@pytest.mark.parametrize("scenario,expected", RESOLVE_MATRIX)
def test_resolve_matrix_via_hf_hub_download(
    scenario, expected,
    live_server_url, hf_api_token, configure_fallback, tmp_path,
):
    """Drive ``hf_hub_download`` against khub for every matrix row.

    On ``"success"`` the downloaded bytes must equal ``OK_BODY`` (no
    proxying corruption). On error rows, the named exception must be
    raised — matching what a hf_hub client talking to HF directly
    would see for the same underlying upstream shape.
    """
    _maybe_skip_disabled(expected)
    configure_fallback(scenario)

    def _run():
        return hf_hub_download(
            repo_id="owner/scenario-repo",
            filename="config.json",
            endpoint=live_server_url,
            token=hf_api_token,
            cache_dir=str(tmp_path),
        )

    if expected == "success":
        path = asyncio.run(asyncio.to_thread(_run))
        assert Path(path).read_bytes() == OK_BODY
    else:
        Exc = _hf_error(expected)
        with pytest.raises(Exc):
            asyncio.run(asyncio.to_thread(_run))


