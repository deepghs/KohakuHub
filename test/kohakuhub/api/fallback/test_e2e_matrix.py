@pytest.mark.parametrize("scenario,expected", PATHS_INFO_MATRIX)
def test_paths_info_matrix_via_hf_api_get_paths_info(
    scenario, expected,
    live_server_url, hf_api_token, configure_fallback,
):
    """Drive ``HfApi.get_paths_info`` against khub for every matrix row.

    ``get_paths_info`` is a POST to ``/api/{type}s/.../paths-info/{rev}``
    and returns a list of ``RepoFile`` / ``RepoFolder`` dataclasses on
    success. Older hf_hub versions used ``HfApi.paths_info`` as the
    method name, so the test branches on attribute presence.
    """
    _maybe_skip_disabled(expected)
    configure_fallback(scenario)
    api = HfApi(endpoint=live_server_url, token=hf_api_token)

    method = getattr(api, "get_paths_info", None) or getattr(api, "paths_info", None)
    if method is None:
        pytest.skip("HfApi has no get_paths_info / paths_info on this version")

    def _run():
        return method(repo_id="owner/scenario-repo", paths=["config.json"])

    if expected == "success":
        result = asyncio.run(asyncio.to_thread(_run))
        # The returned dataclasses carry a `path` attribute; just
        # confirm we got back at least one entry shaped like our
        # OK_PATHS_INFO_JSON fixture.
        assert len(list(result)) >= 1
    else:
        Exc = _hf_error(expected)
        with pytest.raises(Exc):
            asyncio.run(asyncio.to_thread(_run))
