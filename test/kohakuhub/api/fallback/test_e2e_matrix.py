def _hf_error(name: str):
    """Resolve hf_hub exception class across the version matrix.

    - 0.20.x: only ``huggingface_hub.utils.<Name>`` is importable
    - 0.30+ : ``huggingface_hub.errors.<Name>`` is canonical
    """
    try:
        mod = __import__("huggingface_hub.errors", fromlist=[name])
        return getattr(mod, name)
    except (ImportError, AttributeError):
        pass
    mod = __import__("huggingface_hub.utils", fromlist=[name])
    return getattr(mod, name)


@pytest.fixture(scope="module")
def scenario_mock_url():
    """Single uvicorn for all matrix tests in the module — the mock
    is stateless (scenarios are encoded in the URL path), so no
    cross-test pollution."""
    handle = start_live_server(build_scenario_hf_app())
    try:
        yield handle.base_url
    finally:
        stop_live_server(handle)


@pytest.fixture
def configure_fallback(backend_test_state, scenario_mock_url):
    """Per-test helper. ``configure(*scenario_names)`` sets the khub
    fallback chain to one source per scenario name; each source's
    URL ends in ``/scenario/{name}`` so the mock dispatches per the
    matrix row.

    The fallback cache is cleared on entry and exit; the previous
    config is restored after the test.
    """
    cfg = backend_test_state.modules.config_module.cfg
    old_sources = list(cfg.fallback.sources)
    old_enabled = cfg.fallback.enabled
    cfg.fallback.enabled = True

    def _configure(*scenario_names: str):
        cfg.fallback.sources = [
            {
                "url": f"{scenario_mock_url}/scenario/{name}",
                "name": f"Mock-{name}-{i}",
                "source_type": "huggingface",
                "priority": i + 1,
            }
            for i, name in enumerate(scenario_names)
        ]
        backend_test_state.modules.fallback_cache_module.get_cache().clear()

    backend_test_state.modules.fallback_cache_module.get_cache().clear()
    try:
        yield _configure
    finally:
        cfg.fallback.sources = old_sources
        cfg.fallback.enabled = old_enabled
        backend_test_state.modules.fallback_cache_module.get_cache().clear()


